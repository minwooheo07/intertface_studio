-- ================== IF Engine 메타 테이블 (H2 / 로컬 데모) ==================
CREATE TABLE IF_MASTER (
    IF_ID         VARCHAR(30)   PRIMARY KEY,
    IF_NAME       VARCHAR(100),
    SRC_SYSTEM    VARCHAR(30),
    TGT_SYSTEM    VARCHAR(30),
    SRC_TYPE      VARCHAR(10),
    TGT_TYPE      VARCHAR(10),
    SRC_CONFIG    CLOB,
    TGT_CONFIG    CLOB,
    CRON_EXPR     VARCHAR(30),
    DUP_KEY_COLS  VARCHAR(200),
    USE_YN        CHAR(1) DEFAULT 'Y',
    DESCRIPTION   VARCHAR(500)
);

CREATE TABLE IF_MAPPING (
    MAPPING_ID     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IF_ID          VARCHAR(30) NOT NULL,
    SRC_FIELD      VARCHAR(100),
    TGT_FIELD      VARCHAR(100),
    TRANSFORM_RULE VARCHAR(200),
    SORT_ORDER     INT DEFAULT 0
);

CREATE TABLE IF_LOG (
    LOG_ID        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IF_ID         VARCHAR(30),
    EXEC_ID       VARCHAR(36),
    REC_KEY       VARCHAR(200),
    STATUS        VARCHAR(10),
    PAYLOAD_SRC   CLOB,
    PAYLOAD_TGT   CLOB,
    ERROR_MSG     VARCHAR(2000),
    RETRY_OF      BIGINT,
    TRIGGERED_BY  VARCHAR(20),
    CREATED_AT    TIMESTAMP
);

CREATE INDEX IX_IF_LOG_KEY ON IF_LOG (IF_ID, REC_KEY, STATUS);
CREATE INDEX IX_IF_LOG_DT  ON IF_LOG (CREATED_AT);

CREATE TABLE CODE_MAP (
    CODE_MAP_ID   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    GROUP_ID      VARCHAR(30)  NOT NULL,
    SRC_CODE      VARCHAR(50)  NOT NULL,
    TGT_CODE      VARCHAR(50)  NOT NULL,
    DESCRIPTION   VARCHAR(200),
    SORT_ORDER    INT DEFAULT 0,
    CONSTRAINT UX_CODE_MAP UNIQUE (GROUP_ID, SRC_CODE)
);

-- ================== 로컬 데모용 소스/타겟 테이블 ==================
-- 빌링(가상) -> ERP(가상) 매출전표 인터페이스 시연용
CREATE TABLE SRC_DEMO_BILL (
    BILL_NO   VARCHAR(20) PRIMARY KEY,
    CUST_NM   VARCHAR(50),
    AMT       DECIMAL(15,0),
    BILL_YMD  VARCHAR(8),
    IF_FLAG   CHAR(1) DEFAULT 'N'
);

CREATE TABLE TGT_DEMO_ERP (
    SLIP_NO   VARCHAR(20),
    CUST_NM   VARCHAR(50),
    AMOUNT    DECIMAL(15,0),
    SLIP_DT   VARCHAR(10),
    SRC_CD    VARCHAR(10)
);

-- 다우 전자결재 기안 연동 데모용 소스
CREATE TABLE SRC_DEMO_APPROVAL (
    REQ_NO    VARCHAR(20) PRIMARY KEY,
    DRAFTER   VARCHAR(30),
    FORM_ID   VARCHAR(30),
    TITLE     VARCHAR(200),
    CONTENT   VARCHAR(2000),
    IF_FLAG   CHAR(1) DEFAULT 'N'
);
