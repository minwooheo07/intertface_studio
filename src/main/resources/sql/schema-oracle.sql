-- ================== IF Engine 메타 테이블 (Oracle 12c+) ==================
CREATE TABLE IF_MASTER (
    IF_ID         VARCHAR2(30)   PRIMARY KEY,
    IF_NAME       VARCHAR2(100),
    SRC_SYSTEM    VARCHAR2(30),
    TGT_SYSTEM    VARCHAR2(30),
    SRC_TYPE      VARCHAR2(10),
    TGT_TYPE      VARCHAR2(10),
    SRC_CONFIG    CLOB,
    TGT_CONFIG    CLOB,
    CRON_EXPR     VARCHAR2(30),
    DUP_KEY_COLS  VARCHAR2(200),
    USE_YN        CHAR(1) DEFAULT 'Y',
    DESCRIPTION   VARCHAR2(500)
);

CREATE TABLE IF_MAPPING (
    MAPPING_ID     NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IF_ID          VARCHAR2(30) NOT NULL,
    SRC_FIELD      VARCHAR2(100),
    TGT_FIELD      VARCHAR2(100),
    TRANSFORM_RULE VARCHAR2(200),
    SORT_ORDER     NUMBER(5) DEFAULT 0
);

CREATE TABLE IF_LOG (
    LOG_ID        NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IF_ID         VARCHAR2(30),
    EXEC_ID       VARCHAR2(36),
    REC_KEY       VARCHAR2(200),
    STATUS        VARCHAR2(10),
    PAYLOAD_SRC   CLOB,
    PAYLOAD_TGT   CLOB,
    ERROR_MSG     VARCHAR2(2000),
    RETRY_OF      NUMBER(19),
    TRIGGERED_BY  VARCHAR2(20),
    CREATED_AT    TIMESTAMP
);

CREATE INDEX IX_IF_LOG_KEY ON IF_LOG (IF_ID, REC_KEY, STATUS);
CREATE INDEX IX_IF_LOG_DT  ON IF_LOG (CREATED_AT);

CREATE TABLE CODE_MAP (
    CODE_MAP_ID   NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    GROUP_ID      VARCHAR2(30)  NOT NULL,
    SRC_CODE      VARCHAR2(50)  NOT NULL,
    TGT_CODE      VARCHAR2(50)  NOT NULL,
    DESCRIPTION   VARCHAR2(200),
    SORT_ORDER    NUMBER(5) DEFAULT 0,
    CONSTRAINT UX_CODE_MAP UNIQUE (GROUP_ID, SRC_CODE)
);
